void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{

    if(huart->Instance==USART1)
    {
        ESP12S_RevBuf[Revcnt]=ESP12S_RevByte;
        Revcnt++;
        if(Revcnt==ESP12S_BUF_LEN)
        {
            Revcnt=0;
        }
        HAL_UART_Receive_IT(&huart1, &ESP12S_RevByte, 1); //串口1DMA接收数据
    }
		if(huart->Instance==USART2)
    {
				if(Uart2.rx_flag==0)
				{
						Uart2.usart_rxBuf[Uart2.rx_cnt]=Uart2.usart_rev_byte;
						switch(Uart2.rx_cnt)
						{
							case 0:
								if(Uart2.usart_rxBuf[Uart2.rx_cnt]==0x42)
									Uart2.rx_cnt++;
								else
									Uart2.rx_cnt=0;
								break;
							case 1:
								if(Uart2.usart_rxBuf[Uart2.rx_cnt]==0x4D)
									Uart2.rx_cnt++;
								else
									Uart2.rx_cnt=0;
								break;
							case 2:
								Uart2.rx_len = Uart2.usart_rxBuf[Uart2.rx_cnt];
								Uart2.rx_cnt++;
								break;
							case 3:
								Uart2.rx_len = Uart2.usart_rxBuf[Uart2.rx_cnt] + (Uart2.rx_len<<8);
								Uart2.rx_cnt++;
								break;
							default:
								if((Uart2.rx_len+3) == Uart2.rx_cnt && Uart2.rx_len > 0)
								{
									Uart2.rx_cnt=0;
									Uart2.rx_flag=1;
								}
								else
								{
									Uart2.rx_cnt++;
									if(Uart2.rx_cnt>=RECEIVELEN)Uart2.rx_cnt=0;
								}
								break;
						}
				}
				HAL_UART_Receive_IT(&huart2, &Uart2.usart_rev_byte, 1);
		}
}
